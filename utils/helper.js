
/*
* NOTE:
* 1 min = 60,000 ms  => 1000 ms * 60 s
* */
export const ONE_MINUTE = 60_000;
export const TEN_MINUTES = ONE_MINUTE * 10;   // 1 minute * 10  => 10 minutes
export const ONE_HOUR = ONE_MINUTE * 60;      // 1 minute * 60  => 60 minutes
export const ONE_DAY = ONE_HOUR * 24;  // 1m * 1h * 24  => 24 hours
